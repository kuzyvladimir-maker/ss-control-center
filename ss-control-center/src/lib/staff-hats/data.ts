/**
 * Staff Hats — content data for the Staff Hats module.
 *
 * This is the deploy-safe, in-app copy of the wiki under
 * `docs/wiki/staff-hats/` (which lives outside the Vercel root and so cannot
 * be read at runtime). Transcribed faithfully from those leaves on 2026-06-21.
 *
 * We distinguish real, established post hats (`kind: "hat"`) from hiring
 * artifacts: job vacancies (`kind: "vacancy"`) and interview briefs
 * (`kind: "brief"`). Vacancies and briefs must NOT be presented as settled
 * job descriptions — the wiki flags them and so do we.
 *
 * The hat/course CONTENT is intentionally Russian (internal knowledge base).
 * Only the UI chrome (buttons, headers, badges) is English.
 */

