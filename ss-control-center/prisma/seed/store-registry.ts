/** Idempotent seeder — upserts on `id`. */
export async function seedStoreRegistry(
  prisma: import("../../src/generated/prisma/client").PrismaClient
): Promise<number> {
  let count = 0;
  for (const store of STORE_REGISTRY_SEED) {
    await prisma.storeRegistry.upsert({
      where: { id: store.id },
      create: store,
      update: store,
    });
    count++;
  }
  return count;
}
